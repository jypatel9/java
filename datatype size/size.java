import java.lang.*;
class size
{
    public static void main(String arg[])
    {
        System.out.println("minimum size of integer " + Integer.MIN_VALUE);
        System.out.println("minimum size of integer " + Integer.MAX_VALUE);
        System.out.println("size of bite "+ Byte.MIN_VALUE);
        System.out.println("size of byte "+ Byte.MAX_VALUE);
        System.out.println("min of float "+Float.MIN_VALUE);
        System.out.println("max of float "+Float.MAX_VALUE);
        System.out.println("min of double "+Double.MIN_VALUE);
        System.out.println("max of double "+Double.MAX_VALUE);
    }
}